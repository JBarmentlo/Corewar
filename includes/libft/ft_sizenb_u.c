#include "libft.h"

unsigned long	ft_sizenb_u(unsigned long n)
{
	unsigned long	i;

	i = 0;
	if (n == 0)
		return (1);
	while (n != 0)
	{
		n = n / 10;
		i++;
	}
	return (i++);
}
